export default class Item {
  constructor(name, price, quantity) {
    if (
      typeof name !== "string" ||
      typeof price !== "number" ||
      typeof quantity !== "number"
    ) {
      throw new Error("invalid input");
    }
    this.name = name;
    this.price = price;
    this.quantity = quantity;
  }
  updateQuantity(number) {
    if (this.quantity + number > 0) {
      this.quantity += number;
    }
  }
}
